# \t   --  在控制台输出  一个制表符， 协助在输出文本时  垂直方向 保持对齐
print("1 2 3")
print("10 20 30")
print("1\t2\t3")
print("10\t20\t30")

# \n  --  在控制台 输出一个 换行符
print("hello python")
print("hello \npython")

# \\  --   反斜杠符号
print("hello\python")
print("hello\\python")

# \'  --  单引号
print("my'wife")
print("my\'wife")

# \"  --  双引号
print("my\"wife")

# \r  --  回车
print("my\rwife")
